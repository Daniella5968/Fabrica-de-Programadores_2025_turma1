def quadrado(lado):
    area=lado**2
    return area
print(quadrado(10))

def reajuste(salario):
    novo=+salario*0.15
    return novo
print(reajuste(2000))

def triangulo(base, altura): 
    area=(base*altura)/2
    return area
print(triangulo(20, 12))

def graus(celsios):
    F = (9*celsios+160)/5.
    return F
print(graus(100))

def troca(x,y):
    r=y
    y=x
    x = r
    return x,y
print(troca(10,12))










    




    


